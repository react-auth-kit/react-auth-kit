import JwtToken from "../../token/JwtToken";
import {IToken} from "../../token";

describe('JWT Token', () => {
  const token: IToken = new JwtToken();

  it('should parse JWT and return expiration date', () => {
    const jwt = 'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJzdWIiOiIxMjM'+
      '0NTY3ODkwIiwibmFtZSI6IkpvaG4gRG9lIiwiZXhwIjo4MDA4NjA1MTk1fQ.ijw60'+
      '3AjpAqNwnUXmv6YB5L6m5aL-llIgBsTJo-k2r8';
    expect(token.getExpiresAt(jwt)).toStrictEqual(new Date(8008605195000));
  });
});
